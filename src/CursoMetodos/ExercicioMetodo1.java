package CursoMetodos;

public class ExercicioMetodo1 {

	public static void main(String[] args) {
		// 4 operações básicas +, -, * e /
		
		int nota1 = 10;
		int nota2 = 2;
		int soma;
		int subtracao;
		int divisao;
		int multiplicacao;
		
		soma = nota1 + nota2;
		subtracao = nota1 - nota2;
		divisao = nota1 / nota2;
		multiplicacao = nota1 * nota2;
		
		System.out.println("Resultados com os operadores: ");
		System.out.println("A soma das duas notas é: " + soma);
		System.out.println("A subtração das duas notas é: " + subtracao);
		System.out.println("A divisão das duas notas é: " + divisao);
		System.out.println("A multiplicação das duas notas é: " + multiplicacao);

	}

}
